package com.example.kids;

public class SubtractTable {

    private int subtractNum1;
    private int  subtractNum2;
    private int  userAnswer;
    private int correctAnswer;
    private boolean result;

    public SubtractTable() {
    }

    public int getSubtractNum1() {
        return subtractNum1;
    }

    public void setSubtractNum1(int subtractNum1) {
        this.subtractNum1 = subtractNum1;
    }

    public int getSubtractNum2() {
        return subtractNum2;
    }

    public void setSubtractNum2(int subtractNum2) {
        this.subtractNum2 = subtractNum2;
    }

    public int getUserAnswer() {
        return userAnswer;
    }

    public void setUserAnswer(int userAnswer) {
        this.userAnswer = userAnswer;
    }

    public int getCorrectAnswer() {
        return correctAnswer;
    }

    public void setCorrectAnswer(int correctAnswer) {
        this.correctAnswer = correctAnswer;
    }

    public boolean isResult() {
        return result;
    }

    public void setResult(boolean result) {
        this.result = result;
    }


}
